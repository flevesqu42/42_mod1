#include "libft.h"

int		ft_strchr_c(char const *s, int c)
{
	int		i;
	int		j;

	j = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			++j;
		i++;
	}
	return (j);
}
